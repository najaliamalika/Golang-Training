package main

import "fmt"

func main() {
	// Tipe data bool di Go hanya memiliki dua nilai: true dan false
	var isRaining bool = false
	var isWeekend bool = true

	// Menggunakan verb %t untuk mencetak nilai boolean
	fmt.Printf("Apakah sedang hujan? %t\n", isRaining)
	fmt.Printf("Apakah hari ini akhir pekan? %t\n", isWeekend)

	//Menggunakan verb %t untuk menampilkan nilai boolean
}
